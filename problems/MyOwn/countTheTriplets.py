# Given an array of distinct integers. The task is to count all the triplets such that sum of two elements equals the third element.
# Input:
# N = 4
# arr[] = {1, 5, 3, 2}
# Output: 2
# Explanation: There are 2 triplets: 
# 1 + 2 = 3 and 3 +2 = 5 

def count_triplets(nums):
    for i in nums:
        print(i)
 
nums  = [1, 5, 3, 2]

count_triplets(nums)


